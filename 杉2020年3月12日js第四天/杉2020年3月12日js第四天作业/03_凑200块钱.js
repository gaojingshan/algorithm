// 第3题 【凑200块钱】用1元纸币、2元纸币、5元纸币，凑出200块钱，在控制台列出所有凑法。
// 穷举法
// 凑1元纸币
for (var i = 0; i <= 200; i++) {
  // 凑2元纸币
  for (var j = 0; j <= 100; j++) {
    // 凑5元纸币
    for (var z = 0; z <= 40; z++) {
      if (i + 2 * j + 5 * z == 200) {
        console.log(`1元${i}张，2元${j}张，5元${z}张`);
      }
    }
  }
}
