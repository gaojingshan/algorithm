// 【按规则显示数字】显示从100到1000之间所有既能被5整除，也能被6整除的数字，要求每行显示10个数字。
var arr = [];
for (var i = 100; i <= 1000; i++) {
  if (i % 5 == 0 && i % 6 == 0) {
    //   console.log(i);
    arr.push(i); //定义一个数组，保存符合条件的数字
    //当数组长度为10时，输出并把数组清空，再循环输出清空
    while (arr.length == 10) {
      // 把数组转换成字符串显示
      str = '';
      for (var j = 0; j < 10; j++) {
        str += arr[j] + ' ';
      }
      console.log(str);
      arr = [];
    }
  }
}
